# Assignment Operator
print("Assignment Operator")

# What are they?

# = Assign
# += Add and assign
# -= Subtract and assign
# *= Multiply and assign
# /= Divide and assign
# %= Modulus and assign
# **= Exponent and assign
# //= Floor Division and assign

number = 10
print(number)

number += 5
number = number + 5
print(number)

number -= 2
number = number - 2
print(number)

number *= 2
number = number *2
print(number)

number /= 2
number = number / 2
print(number)

number %= 2
number = number % 2
print(number)

