# Comparison Operators
print("Comparison Operators")

# What are they?

# == Equal to
# != Not equal to
# > Greater than
# < Less than
# >= Greater than or equal to
# <= Less than or equal to

num1 = 5
num2 = 5
num3 = 15

print(num1 == num2)
print(num1 == num3)
print(num1 != num3)
print(num1 > num3)
print(num1 > num2)
print(num1 < num2)
print(num1 < num3)
print(num1 <= num2)
print(num1 <= num3)
print(num1 >= num2)
print(num1 >= num3)